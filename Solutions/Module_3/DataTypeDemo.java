public class DataTypeDemo {

    public static void main(String[] args) {

        // Declaring different primitive data types
        int number = 25;

        float percentage = 89.5f;

        double salary = 45000.75;

        char grade = 'A';

        boolean isJavaEasy = true;

        // Displaying values
        System.out.println("Integer value : " + number);

        System.out.println("Float value : " + percentage);

        System.out.println("Double value : " + salary);

        System.out.println("Character value : " + grade);

        System.out.println("Boolean value : " + isJavaEasy);
    }
}